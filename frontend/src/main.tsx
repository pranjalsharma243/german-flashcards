function updateSrsEntry(data: SrsData, cardId: string, quality: 'again' | 'good' | 'easy'): SrsData {
  const now = Date.now();
  const prev = data[cardId];
  const MIN = 5 * 60 * 1000; // 5 minutes
  let interval: number, reps: number;
  if (quality === 'again') { interval = MIN; reps = 0; }
  else if (quality === 'good') { interval = prev ? prev.interval * 2 : MIN * 2; reps = (prev?.reps ?? 0) + 1; }
  else { interval = prev ? prev.interval * 3 : MIN * 3; reps = (prev?.reps ?? 0) + 1; }
  return { ...data, [cardId]: { cardId, interval, nextReview: now + interval, reps } };
}


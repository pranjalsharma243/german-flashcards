function buildDD(cards: CardItem[], ki: Set<string>, pi: Set<string>) {
  const dist = [
    { name: 'Known', value: cards.filter(c => ki.has(c.id)).length, fill: PIE_COLORS.known },
    { name: 'Practice', value: cards.filter(c => pi.has(c.id)).length, fill: PIE_COLORS.practice },
    { name: 'New', value: cards.filter(c => !ki.has(c.id) && !pi.has(c.id)).length, fill: PIE_COLORS.fresh },
  ];
  const tm = new Map<string, { type: string; known: number; practice: number; fresh: number }>();
  for (const c of cards) {
    const e = tm.get(c.type) ?? { type: c.type, known: 0, practice: 0, fresh: 0 };
    if (ki.has(c.id)) e.known++; else if (pi.has(c.id)) e.practice++; else e.fresh++;
    tm.set(c.type, e);
  }
  return { dist, types: [...tm.values()].sort((a, b) => (b.known + b.practice + b.fresh) - (a.known + a.practice + a.fresh)) };
}


function escapeRegex(s: string): string { return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'); }

function generateSentence(card: CardItem): GeneratedSentence {
  const templates = SENTENCE_TEMPLATES[card.type] ?? FALLBACK_TEMPLATES;
  const tpl = templates[Math.floor(Math.random() * templates.length)];
  const fullDe = tpl.de(card.word);
  const blankedDe = fullDe.replace(new RegExp(escapeRegex(card.word), 'i'), '____');
  return { fullDe, blankedDe, answer: card.word, en: tpl.en(card.english), hi: tpl.hi(card.hindi) };
}


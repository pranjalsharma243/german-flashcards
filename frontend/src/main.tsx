function generateMcqOptions(card: CardItem, allCards: CardItem[], ansLang: AnswerLanguage, reversed: boolean): string[] {
  const correctAnswer = reversed ? (card.article ? `${card.article} ${card.word}` : card.word) : card[ansLang];
  const wrongPool = allCards.filter(c => c.id !== card.id).map(c => reversed ? (c.article ? `${c.article} ${c.word}` : c.word) : c[ansLang]);
  const unique = [...new Set(wrongPool)].filter(w => w !== correctAnswer);
  // shuffle and pick 3
  for (let i = unique.length - 1; i > 0; i--) { const j = Math.floor(Math.random() * (i + 1)); [unique[i], unique[j]] = [unique[j], unique[i]]; }
  const wrongs = unique.slice(0, 3);
  const options = [correctAnswer, ...wrongs];
  // shuffle options
  for (let i = options.length - 1; i > 0; i--) { const j = Math.floor(Math.random() * (i + 1)); [options[i], options[j]] = [options[j], options[i]]; }
  return options;
}

